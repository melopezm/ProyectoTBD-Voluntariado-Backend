package voluntariado.demo.models;

public class TaskAbility {

    private Integer id;
    private Integer id_emehab;
    private Integer id_habilidad;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getId_emehab() {
        return id_emehab;
    }

    public void setId_emehab(Integer id_emehab) {
        this.id_emehab = id_emehab;
    }

    public Integer getId_habilidad() {
        return id_habilidad;
    }

    public void setId_habilidad(Integer id_habilidad) {
        this.id_habilidad = id_habilidad;
    }
}
